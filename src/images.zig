const w4 = @import("wasm4.zig");

fn Sprite(
    comptime w: u16,
    comptime h: u16,
    comptime bpp: u8,
    colormap: u16,
    image: [@as(u16, w) * h / (8 / bpp)]u8,
) type {
    return struct {
        pub fn render(x: i32, y: i32, flip: bool) void {
            w4.DRAW_COLORS.* = colormap;
            const blit_flag = if (bpp == 1)
                w4.BLIT_1BPP
            else if (bpp == 2)
                w4.BLIT_2BPP
            else
                unreachable;
            const flip_flag = if (flip) w4.BLIT_FLIP_X else 0;
            w4.blit(
                &image,
                x,
                y,
                w,
                h,
                blit_flag | flip_flag,
            );
        }
    };
}

