const w4 = @import("wasm4.zig");

fn Sprite(w: u8, h: u8) type {
    return struct {
        const tile_width = w;
        const tile_height = h;
        image: [w * 4 * h * 16]u8,
        const Self = @This();
        pub fn render(sprite: *const Self, x: i32, y: i32) void {
            w4.DRAW_COLORS.* = 0x4321;
            w4.blit(
                &sprite.image,
                x * 16 - 8,
                y * 16 - 8,
                tile_width * 16,
                tile_height * 16,
                w4.BLIT_2BPP,
            );
        }
    };
}

